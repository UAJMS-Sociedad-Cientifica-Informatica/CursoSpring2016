package modelos;

public class MUsuario {
	
	int persona,red_salud,municipio,centro_salud;
	String rol,alcanze,login,passwd;
	Boolean activo;
	
	
	public int getPersona() {
		return persona;
	}
	public void setPersona(int persona) {
		this.persona = persona;
	}
	public int getRed_salud() {
		return red_salud;
	}
	public void setRed_salud(int red_salud) {
		this.red_salud = red_salud;
	}
	public int getMunicipio() {
		return municipio;
	}
	public void setMunicipio(int municipio) {
		this.municipio = municipio;
	}
	public int getCentro_salud() {
		return centro_salud;
	}
	public void setCentro_salud(int centro_salud) {
		this.centro_salud = centro_salud;
	}
	public String getRol() {
		return rol;
	}
	public void setRol(String rol) {
		this.rol = rol;
	}
	public String getAlcanze() {
		return alcanze;
	}
	public void setAlcanze(String alcanze) {
		this.alcanze = alcanze;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public Boolean getActivo() {
		return activo;
	}
	public void setActivo(Boolean activo) {
		this.activo = activo;
	}
	
	

}
